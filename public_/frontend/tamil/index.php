  </p>















<ul><li><em>Pre-connection</em><strong>:</strong><em> </em>in this section, we still don't know much about penetration testing -- all we have is a computer with a wireless card. You will learn how <strong>gather information</strong> about the networks and computers around you and launch a number of attacks without a password, such as controlling the connections around you (ie: <strong>deny/allow any device from connecting to any network</strong>). You will also learn how to <strong>create a fake access</strong> <strong>point,</strong> attract users to connect to it and capture any important information they enter.</li><li><em>Gaining Access</em>: Now that you have gathered information about the networks around you and found your target, you will learn how to crack the key and gain access to your target network. In this section you will learn a <strong>number of methods to crack WEP/WPA/WPA2 </strong>encryption.</li><li> <em>Post Connection</em>: Now you have the key to your target network and you can connect to it. In this section you will learn a number of<strong> powerful attacks</strong> that can be launched against the network and connected clients. These attacks will allow you to gain <strong>access to any account accessed by any device</strong> connected to your network and <strong>read all the traffic used by these devices</strong> (images, videos, audio, passwords ...etc).</li></ul>



























<p><em class="redactor-inline-converted">2. Gaining Access - </em>In this section you will learn two main approaches to <strong>gain full control over any computer system</strong>:<br></p>















<ul><li><em>Server Side Attacks:  </em>In this approach you will learn how to <strong>gain full access</strong> to computer systems <strong>without the need for user interaction</strong>. You will learn how to gather information about a target computer system such as its operating system, open ports, installed services and <strong>discover weaknesses and vulnerabilities</strong>. You will also learn how to <strong>exploit</strong> these weaknesses to gain full control over the target. Finally you will learn how to generate different types of <strong>reports</strong> for your discoveries.</li><li><em>Client Side Attacks - </em>If the target system does not contain any weaknesses then the only way to gain access to it is by interacting with the user. In this approach you will learn how to launch a number of powerful attacks to fool the target user and get them to install a backdoor on their device. This is done by <strong>creating fake updates</strong> and serving them to the user or by <strong>backdoornig downloaded</strong> <strong>files</strong> on the fly. You will also learn how to <strong>gather information about the target person</strong> and use <strong>social engineering</strong> to deliver a <strong>backdoor to them as an image or any other file type.</strong></li></ul>



























<p><em class="redactor-inline-converted">3. Post Exploitation - </em>In this section you will learn how to interact with the systems you compromised so far. Youâ€™ll learn <strong>how to access the file system (read/write/upload/execute)</strong>, <strong>maintain your access</strong>, spy on the target and even <strong>use the target computer as a pivot to hack other computer systems.</strong><br></p>



























<p><em class="redactor-inline-converted">4. Web Application Penetration Testing - </em>In this section you will learn <strong>how websites work</strong>. Then you will learn how to <strong>gather information</strong> about your target website, such as website owner, server location, used technologies and much more. You will also learn how to <strong>discover and exploit a number of dangerous vulnerabilities such as SQL injections, XSS vulnerabilities</strong>, <strong>etc.</strong>

  </p>



























<p>At the end of each section you will learn how to <strong>detect, prevent and secure your system and yourself from these attacks</strong>.

